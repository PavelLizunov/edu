interface IconProps extends Omit<LucideProps, "ref"> {
  name: string;
  fallback?: string;
}

/**
 * Lucide icon by string name. Used in MDX (ConceptCard icon="Boxes") and category
 * config, where the icon is data, not a JSX import.
 */
export function Icon({ name, fallback = "Boxes", ...props }: IconProps) {
  const Cmp = ICONS[name] ?? ICONS[fallback] ?? Boxes;
  return <Cmp strokeWidth={1.75} {...props} />;
}
